package Javasessions;

public class FinalKeyword {

	public static void main(String[] args) {
	
		// constant values : final is a keyword used for  declaring variable with constant value  
		
		int x =10;
		x=20;
		x=30;
		System.out.println(x);
		
		final int days =7; // value is fixed and no one can change it 
		//days =10;
		int salary = days *100;
		System.out.println(salary);
		
		final int DEFAULT_TIME_OUT =10; // final variable always write in capital letter 
		final String LOGIN_PAGE_TITLE ="Amazon Login";
		final char GENDER ='f';
		
		
		
		
		
		
	}

}
